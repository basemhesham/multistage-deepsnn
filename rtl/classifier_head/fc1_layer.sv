// Goal      : Fully-connected layer 1: 128 inputs -> 256 outputs + ReLU.
//             Uses 8 parallel MAC units with time-multiplexed dot-product
//             computation across 32 batches. Q7.10 fixed-point.
//             Weight deduplication: MAP indices in 8 BRAM36 (4Kx9 each),
//             unique weights (375 values) in combinational LUT ROM.
//             1-cycle BRAM pipeline: 129 cycles/batch, 4,128 total.
